// 模拟您的OLED_ShowPicture函数
void OLED_ShowPicture(int x, int y, int sizex, int sizey, const uint8_t BMP[], int mode) {
    uint16_t j = 0;
    uint8_t i, m, temp;
    uint8_t x0 = x, y0 = y;
    sizey = sizey / 8 + ((sizey % 8) ? 1 : 0);
    
    for (uint8_t n = 0; n < sizey; n++) {
        x = x0;
        y0 = y + n * 8;
        for (i = 0; i < sizex; i++) {
            temp = BMP[j];
            j++;
            for (m = 0; m < 8; m++) {
                if (temp & 0x01) {
                    // 根据mode决定是否镜像显示
                    int display_x = (mode == 1) ? (x + sizex - 1 - i) : (x + i);
                    int display_y = y0 + m;
                    
                    if (display_x >= 0 && display_x < WIDTH && display_y >= 0 && display_y < HEIGHT) {
                        oled_draw_pixel(display_x, display_y, 1);
                    }
                }
                temp >>= 1;
            }
        }
    }
}

int main() {
    oled_init();
    
    // 清空缓冲区
    memset(oled_buffer, 0, sizeof(oled_buffer));
    
    // 显示开机图像
    OLED_ShowPicture(0, 0, 128, 64, boot_logo, 1);
    
    int running = 1;
    while (running) {
        SDL_Event event;
        while (SDL_PollEvent(&event)) {
            if (event.type == SDL_QUIT) {
                running = 0;
            }
        }
        
        oled_update_display();
        SDL_Delay(16); // 约60FPS
    }
    
    oled_cleanup();
    return 0;
}